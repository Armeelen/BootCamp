season=input("Please enter the current month: ").upper()


if season=="MARCH"or season=="APRIL"or season=="MAY":
    print("Spring has come")

elif season=="JUNE" or season=="JULY" or season=="AUGUST":
    print("It's Summer")

elif season=="SEPTEMBER" or season=="OCTOBER" or season=="NOVEMBER":
    print("Autumn has come")

elif season=="DECEMBER" or season=="JUNUARY" or season=="FEBRUARY":
    print("Its finally Winter!!!")

else:
    print("Sorry, thats not a valid season, please, Retry!")
    

